# Imagine you have string variables:

adj = "Good"
part_of_day = "morning"
comma = ','
title = "Ms."
surname = "Johnson"

# And you need to print out a greeting "Good morning, Ms. Johnson!"
# (without the quotes) calling the print() function
# only once and specifying its keyword arguments, without adding
# any other strings. How would you do that?

print(adj, part_of_day + comma, title, surname, end='!')