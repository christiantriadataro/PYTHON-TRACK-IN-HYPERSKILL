# Theory: Regexps in Python

# A regular expression is a sequence of characters defining a
# search pattern, that is, a pattern describing a set of strings.
# These patterns are used for searching and editing text,
# replacing one substring with another, and so on. The simplest
# example of using a regular expression is when we search for
# some word in a text file or on a web page. For example, if we
# look for the word "python", the string "python" becomes a
# simple regular expression - a search pattern that corresponds
# only to the word "python" and nothing else. More complicated
# regular expressions will be able to match a larger number of
# strings.

